package hu.progmasters.commonAbstracts;

public abstract class SiegeMachines {
    protected int damage;
    protected int cost;

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public int getCost() {
        return cost;
    }

    public void setCost(int cost) {
        this.cost = cost;
    }
}
